    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter a value for Initial Investment.");
        } else if (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField1.getText()))) == false) { 
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Initial Investment must be numeric. Try again!");
        } else if (Double.parseDouble(jTextField1.getText()) < 0) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Initial Investment cannot be negative. Try again!");
        }
        
        else if (jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty())  {
            JOptionPane.showMessageDialog(null, "Enter values for Annual and Monthly Contribution.");        

        }
        else if ( (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField2.getText()))) == false) || (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField3.getText()))) == false) ) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Annual and Monthly Contribution must both be numeric. Try again!");                    
        }
      
        else if ((Double.parseDouble(jTextField2.getText()) == 0) && (Double.parseDouble(jTextField3.getText()) == 0)) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Annual and Monthly Contributions cannot both be 0. Try again!");                    
        }    
        else if (Double.parseDouble(jTextField2.getText()) != 0 & Helper.isPos(String.valueOf(Double.parseDouble(jTextField2.getText())))  == false) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Annual Contribution must be greater than or equal to 0. Try again!");
        }         
        else if (Double.parseDouble(jTextField3.getText()) != 0 & Helper.isPos(String.valueOf(Double.parseDouble(jTextField3.getText()))) == false) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Monthly Contribution must be greater than or equal to 0. Try again!");
        }
        
        else if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a value for Interest Rate.");
        } else if ((Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField4.getText()))) == false) || Math.abs(Double.parseDouble(jTextField4.getText())) > 100) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Interest Rate must be between -100 and 100. Try again!");
        }
        
        else if (jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty())  {
            JOptionPane.showMessageDialog(null, "Enter values for Investment Length.");        
        } else if ( (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField6.getText()))) == false) || (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField7.getText()))) == false) ) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Investment Length must be numeric. Try again!");                    
        } else if ((Double.parseDouble(jTextField6.getText()) == 0) && (Double.parseDouble(jTextField7.getText()) == 0)) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Investment Length cannot be 0. Try again!");                    
        } else if (Double.parseDouble(jTextField6.getText()) < 0 || (Double.parseDouble(jTextField7.getText()) < 0) ) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Investment Length must be a positive number. Try again!");
        }
       
        else if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter a value for Inflation Rate.");
        } else if (Helper.isValidNumber(String.valueOf(Double.parseDouble(jTextField8.getText()))) == false) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Inflation Rate must be between 0 and 100. Try again!");
        } else if (Math.abs(Double.parseDouble(jTextField8.getText())) > 100) {
            JOptionPane.showMessageDialog(null, "INPUT INVALID: Inflation Rate must be between 0 and 100. Try again!");
        }
        
        String selectedItem = (String) jComboBox1.getSelectedItem();
        double timesCompounded;
        if (selectedItem.equals("annually")) {
            timesCompounded = 1;
        } else if (selectedItem.equals("semiannually")) {
            timesCompounded = 2;
        } else if (selectedItem.equals("quarterly")) {
            timesCompounded = 4;
        } else if (selectedItem.equals("monthly")) {
            timesCompounded = 12;
        } else if (selectedItem.equals("biweekly")) {
            timesCompounded = 26.0715;
        } else if (selectedItem.equals("weekly")) {
            timesCompounded = 52.143;
        } else if (selectedItem.equals("daily")) {
            timesCompounded = 365;
        } else {
            timesCompounded = Math.E;
        }
        
        panel1.setVisible(true);
        double endingBal = (double) interestHelper.findIntWithTax(Double.parseDouble(jTextField1.getText()), 0, Double.parseDouble(jTextField4.getText()), timesCompounded, Double.parseDouble(jTextField6.getText()), Double.parseDouble(jTextField7.getText()), Double.parseDouble(jTextField8.getText()), Double.parseDouble(jTextField2.getText()), Double.parseDouble(jTextField3.getText()));
        jLabel33.setText(String.format("$%.2f",endingBal));
        double principle = Double.parseDouble(jTextField1.getText()) + ((Double.parseDouble(jTextField2.getText()) + (12 * Double.parseDouble(jTextField3.getText()))) * (Double.parseDouble(jTextField6.getText()) + (Double.parseDouble(jTextField7.getText()) / 12)) );
        jLabel28.setText(String.format("$%.2f", principle));
        //double contributions = ((Double.parseDouble(jTextField2.getText()) + 12 * (Double.parseDouble(jTextField3.getText())))) * (Double.parseDouble(jTextField6.getText()) + (12 * Double.parseDouble(jTextField7.getText())));
        //jTextField10.setText(String.format("%.2f", contributions));
        double displayInt = endingBal - principle;
        jLabel31.setText(String.format("$%.2f", displayInt));
        double inflatedVal = (double) interestHelper.handleInflation(endingBal, Double.parseDouble(jTextField8.getText()), Double.parseDouble(jTextField6.getText()), Double.parseDouble(jTextField7.getText()));
        jLabel32.setText(String.format("$%.2f", inflatedVal));
        double totalContr = (double) interestHelper.displayContributions(Double.parseDouble(jTextField4.getText()), timesCompounded, Double.parseDouble(jTextField6.getText()), Double.parseDouble(jTextField7.getText()), Double.parseDouble(jTextField2.getText()), Double.parseDouble(jTextField3.getText()));
        //jLabel30.setText(String.format("$%.2f", totalContr));
        double contrWOInterest = interestHelper.contributionsNoInt(Double.parseDouble(jTextField6.getText()), Double.parseDouble(jTextField7.getText()), Double.parseDouble(jTextField2.getText()), Double.parseDouble(jTextField3.getText()));
        jLabel29.setText(String.format("$%.2f", contrWOInterest));
        jLabel30.setText(String.format("$%.2f", contrWOInterest + displayInt));
    }                                     

